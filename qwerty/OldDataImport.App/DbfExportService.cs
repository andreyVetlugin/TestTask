        public static void Export()
        {

            var config = new ConfigurationBuilder()
                .AddJsonFile("./configuration.json")
                .Build()
                .Get<ImportConfig>();

            using (var dbContext = new BenefitsContext(new DbContextOptionsBuilder()
                .UseNpgsql(config.ConnectionStrings.BenefitsContext).Options))
            {
                var readDbContext = new ReadDbContext<IBenefitsEntity>(dbContext);

                var date = DateTime.Now;

                var updates = readDbContext.Get<GosPensionUpdate>()
                    .ActualAt(date.Year, date.Month)
                    .ToList();

                var personInfos = readDbContext.Get<PersonInfo>()
                    .ToDictionary(p => p.RootId);

                var pens = updates
                    .Select(u =>
                    {
                        var personInfo = personInfos[u.PersonInfoRootId];

                        return new Pens
                        {
                            RN = 0.ToString(),
                            NPD = personInfo.PensionCaseNumber.ToString(),
                            FM = personInfo.SurName,
                            IM = personInfo.Name,
                            OT = personInfo.MiddleName,
                            DTR = personInfo.BirthDate.ToString("dd.MM.yyyy"),
                            TLF = personInfo.Phone,
                            T_SUMP = u.Amount
                        };
                    })
                    .Where(r => Filter(r.FM, r.IM, r.OT));

                DbfReaderHelper.WriteAll<IPens>("./data/Result/pens.dbf", pens);
            }
        }

        static bool Filter(string surName, string firstName, string middleName)
        {
            var fullName = $"{surName} {firstName} {middleName}";

            return FilterFios.Contains(fullName) && !ExcludeFilter.Contains(fullName);
        }

